	public class GeneratedFile
	{
		public GeneratedFile(string path, string source)
		{
			Path = path;
			// Sha256 explicit so expected matches what the running compilation produces.
			// Roslyn 5's CompilationOptions.ChecksumAlgorithm defaults to Sha256, but the
			// SourceText.From(string, Encoding) overload still defaults to Sha1 — pinning
			// Sha256 here aligns the expected SourceText with the generator's actual output.
			Source = SourceText.From(source, Encoding.UTF8, SourceHashAlgorithm.Sha256);
		}

		public string Path { get; set; }

		public SourceText Source { get; set; }
	}
