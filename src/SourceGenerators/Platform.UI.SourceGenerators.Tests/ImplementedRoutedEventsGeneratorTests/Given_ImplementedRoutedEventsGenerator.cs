";
			await TestGeneratorAsync(
				inputSource,
				new GeneratedFile(
					@"CodeBrix.Platform.UI.SourceGenerators\CodeBrix.Platform.UI.SourceGenerators.ImplementedRoutedEvents.ImplementedRoutedEventsGenerator\MyAwesomeControl_ImplementedRoutedEvents.g.cs",
					expectedCode));
		}

