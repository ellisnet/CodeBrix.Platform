	using Verify = CSharpSourceGeneratorVerifier<ImplementedRoutedEventsGenerator>;

	[TestClass]
	public class Given_ImplementedRoutedEventsGenerator
	{
		private async Task TestGeneratorAsync(string inputSource, params GeneratedFile[] generatedFiles)
		{
			var test = new Verify.Test
			{
				// Use the local de-Uno'd assemblies (which provide the CodeBrix.Platform.* namespaces emitted by
				// the generator) rather than the Uno.WinUI NuGet (which only provides the Uno.* namespaces).
				ReferenceAssemblies = _Dotnet.Current.ReferenceAssemblies,
				TestState =
				{
					Sources = { inputSource },
				},
			};
			test.TestState.AdditionalReferences.AddRange(CodeBrixAssemblyHelper.LoadAssemblies());
			// Generated-file hint paths are authored with Windows separators; align to the running OS
			// ('\' on Windows, '/' elsewhere) so the expected file list matches on every platform.
			test.TestState.GeneratedSources.AddRange(generatedFiles.Select(f => (f.Path.Replace('\\', System.IO.Path.DirectorySeparatorChar), f.Source)));
			await test.RunAsync();
		}

		[TestMethod]
		public async Task Given_NonGeneric_Control_In_Global_Namespace()
		{
			const string inputSource = @"
using Microsoft.UI.Xaml.Controls;

public partial class MyAwesomeControl : Control
{
}
";
			const string expectedCode = @"// <auto-generated>
partial class MyAwesomeControl
{
	[global::System.ComponentModel.EditorBrowsable(global::System.ComponentModel.EditorBrowsableState.Never)]
	private static global::CodeBrix.Platform.UI.Xaml.RoutedEventFlag __codebrix_ImplementedRoutedEvents = global::CodeBrix.Platform.UI.Xaml.UIElementGeneratedProxy.RegisterImplementedRoutedEvents(
		typeof(MyAwesomeControl),
		global::CodeBrix.Platform.UI.Xaml.RoutedEventFlag.None
	);
