        private CompilationUnitSyntax Generate(CompilationUnitSyntax code, WaylandProtocol protocol)
        {
            code = code.AddUsings(UsingDirective(IdentifierName("System")))
                .AddUsings(UsingDirective(IdentifierName("System.Runtime.CompilerServices")))
                .AddUsings(UsingDirective(IdentifierName("System.Runtime.InteropServices")))
                .AddUsings(UsingDirective(IdentifierName(InteropNs)))
                .AddUsings(UsingDirective(IdentifierName(CoreProtocolNs)));

            var ns = NamespaceDeclaration(ProtocolNamespaceSyntax(protocol.Name))
                .WithLeadingTrivia(
                    TriviaList(
                        Trivia(NullableDirectiveTrivia(Token(SyntaxKind.EnableKeyword), true))))
                .NormalizeWhitespace();

            // The top-level interface type carries the binding's accessibility. Nested members
            // (Server, Listener, enums, …) stay public but are effectively scoped by this — so
            // an internal interface type makes the whole binding assembly-private in one place.
            var accessibility = _internalProtocols.Contains(protocol.Name)
                ? SyntaxKind.InternalKeyword
                : SyntaxKind.PublicKeyword;

            foreach (var @interface in protocol.Interfaces)
            {
                var cl = ClassDeclaration(Pascalize(@interface.Name))
                    .WithModifiers(new SyntaxTokenList(
                        Token(accessibility),
                        Token(SyntaxKind.SealedKeyword),
                        Token(SyntaxKind.UnsafeKeyword),
                        Token(SyntaxKind.PartialKeyword)))
                    .AddBaseListTypes(
                        SimpleBaseType(ParseTypeName("WlProxy")),
                        SimpleBaseType(ParseTypeName("global::CodeBrix.Platform.WinUI.Runtime.Skia.Wayland.Interop.IWlProxyTypeDescriptorProvider")));
                cl = WithSummary(cl, @interface.Description);
                cl = WithSignature(cl, @interface);
                cl = WithRequests(cl, protocol, @interface);
                cl = WithEvents(cl, protocol, @interface);
                cl = WithEnums(cl, protocol, @interface);
                // CodeBrix fork: server-side codegen (WithServer / server listeners) is disabled —
                // this head is a pure Wayland CLIENT. The Server generator sources are kept in the
                // frozen snapshot for diffability with upstream but are never invoked.
                
                cl = cl.AddMembers(ConstructorDeclaration(cl.Identifier)
                    .AddModifiers(Token(SyntaxKind.PrivateKeyword))
                    .WithParameterList(ParameterList(
                        SeparatedList(new[]
                        {
                            Parameter(Identifier("context")).WithType(ParseTypeName("global::CodeBrix.Platform.WinUI.Runtime.Skia.Wayland.Interop.WlProxyCreationContext")),
                        }))).WithBody(Block())
                    .WithInitializer(ConstructorInitializer(SyntaxKind.BaseConstructorInitializer,
                        ArgumentList(SeparatedList(new[]
                        {
                            Argument(IdentifierName("context")),
                        })))));
                cl = cl.AddMembers(ParseMemberDeclaration(@$"
                public static {cl.Identifier} Import(WlDisplay display, IWlTargetQueue? queue, IntPtr handle, bool ownsHandle, IWlEventsListener? listener)
                {{
                    return ({cl.Identifier})WlProxy.Import(ProxyType, display, queue, handle, ownsHandle, listener);
                }}")!);
                
                ns = ns.AddMembers(cl);
            }

            return code.AddMembers(ns);
        }
    }
