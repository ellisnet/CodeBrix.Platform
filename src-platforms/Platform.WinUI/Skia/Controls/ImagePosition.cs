namespace CodeBrix.Platform.WinUI.Controls;

/// <summary>
/// Specifies the position of the image relative to the text
/// in an <see cref="EmbeddedImageButton"/>.
/// </summary>
public enum ImagePosition
{
    /// <summary>The image appears to the left of the text.</summary>
    Left,

    /// <summary>The image appears above the text.</summary>
    Top,

    /// <summary>The image appears to the right of the text.</summary>
    Right,

    /// <summary>The image appears below the text.</summary>
    Bottom
}
